        model = (DefaultTableModel) tbl_INVENMANA.getModel();
        model.setRowCount(0);
        listSP = new QLKHODAO().selectAll();
        for (INVENTORY wh : listSP) {
            model.addRow(new Object[]{wh.getMASP(), wh.getTENSP(), wh.getHINH(),
                wh.getDONGIA(), wh.getSOLUONG(), wh.getGHICHU()});
        }
